// Section headers
// http://msdn.microsoft.com/en-us/library/windows/desktop/ms680341(v=vs.85).aspx
// http://doxygen.reactos.org/d5/d44/ntimage_8h_a98e3229bbf99bf35eaebf248b173d235.html#a98e3229bbf99bf35eaebf248b173d235
#define IMAGE_SIZEOF_SHORT_NAME   8
typedef struct s_SectionHeader {
  char Name[IMAGE_SIZEOF_SHORT_NAME];
  union {
    uint32_t PhysicalAddress;
    uint32_t VirtualSize;
  } Misc;
  uint32_t VirtualAddress;
  uint32_t SizeOfRawData;
  uint32_t PointerToRawData;
  uint32_t PointerToRelocations;
  uint32_t PointerToLinenumbers;
  uint16_t NumberOfRelocations;
  uint16_t NumberOfLinenumbers;
  uint32_t Characteristics;
} SectionHeader;

